package main;

public class MathUtil {
	public int add(int a,int b) {
		return a + b;
	}
	public int sub (int a, int b) {
		return a - b;
	}
	
	public int multiplies (int a, int b) {
		return a * b;
	}
	public int divides (int a, int b) {
		return a / b;
	}
	public double circumference (double r) {
		return 2 * Math.PI * r;
	}
}

